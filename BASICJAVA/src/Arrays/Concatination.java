package Arrays;

public class Concatination {

	public static void main(String[] args) 
	{
		int i=10;
		int j=20;
		String name="Anand";
		
		 System.out.println(i+j); 
		 System.out.println(i+j+name);
		 System.out.println(i+name+j); 
		 System.out.println(name+i+j);
		 System.out.println(name+(i+j));
		 
		//System.out.print("");this help us to make one line statement from starting
		//System.out.println();this'll come as vertically after one line
	}

}
